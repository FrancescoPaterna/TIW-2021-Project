package beans;

public class Course {
	private int id;
	private String name;
	private int IDprofessor;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIDprofessor() {
		return IDprofessor;
	}
	public void setIDprofessor(int iDprofessor) {
		IDprofessor = iDprofessor;
	}
	
	
	

}
