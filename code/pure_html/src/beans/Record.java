package beans;
import java.util.Date;

public class Record {
	
	private int id; 
	private Date date;
	private int IDExamDate;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getIDExamDate() {
		return IDExamDate;
	}
	public void setIDExamDate(int iDExamDate) {
		IDExamDate = iDExamDate;
	}
	
	
	
}
